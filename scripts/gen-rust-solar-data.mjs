#!/usr/bin/env node
/**
 * Generate the Rust data tables for the native solar-core port from the
 * canonical TypeScript source, so the Rust crate stays a faithful translation
 * with a single source of truth.
 *
 *   src/vsop87d-earth.ts  EARTH_L, EARTH_R  -> rust/src/vsop87d_earth.rs
 *   src/astro.ts          NUT_COEFFS        -> rust/src/nutation_data.rs
 *
 * Usage: node scripts/gen-rust-solar-data.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

/** Extract a top-level array literal `const NAME ... = [ ... ];` and eval it. */
function extractArray(src, name) {
  const decl = src.indexOf(`const ${name}`);
  if (decl < 0) throw new Error(`${name} not found`);
  const start = src.indexOf('[', src.indexOf('=', decl));
  let depth = 0;
  let i = start;
  for (; i < src.length; i++) {
    if (src[i] === '[') depth++;
    else if (src[i] === ']' && --depth === 0) { i++; break; }
  }
  // eslint-disable-next-line no-eval
  return eval(src.slice(start, i));
}

/** f64 literal that round-trips and is always a valid Rust float (has . or e). */
const f = (x) => {
  const s = x.toString();
  return /[.eE]/.test(s) ? s : `${s}.0`;
};

const earth = readFileSync(join(root, 'src/vsop87d-earth.ts'), 'utf8');
const EARTH_L = extractArray(earth, 'EARTH_L');
const EARTH_R = extractArray(earth, 'EARTH_R');

const series = (s) =>
  '&[\n' +
  s.map((terms) => '    &[' + terms.map(([a, b, c]) => `[${f(a)}, ${f(b)}, ${f(c)}]`).join(', ') + ']').join(',\n') +
  '\n]';

const header =
  '// @generated by scripts/gen-rust-solar-data.mjs from the TypeScript source.\n' +
  '// Do not edit by hand; re-run the generator to update.\n' +
  '// VSOP87 phase coefficients legitimately sit near math constants (e.g. π),\n' +
  '// which is not a sloppy approximation here.\n' +
  '#![allow(clippy::approx_constant)]\n\n';
const vsopRs =
  header +
  '/// VSOP87D Earth heliocentric longitude L, shape [series][term][A, B, C];\n' +
  '/// series n is multiplied by tau^n, each term computes A*cos(B + C*tau).\n' +
  `#[rustfmt::skip]\npub static EARTH_L: &[&[[f64; 3]]] = ${series(EARTH_L)};\n\n` +
  '/// VSOP87D Earth heliocentric radius R (AU), same [series][term][A, B, C] shape.\n' +
  `#[rustfmt::skip]\npub static EARTH_R: &[&[[f64; 3]]] = ${series(EARTH_R)};\n`;
writeFileSync(join(root, 'rust/src/vsop87d_earth.rs'), vsopRs);

const astro = readFileSync(join(root, 'src/astro.ts'), 'utf8');
const NUT = extractArray(astro, 'NUT_COEFFS');
const nutRs =
  header +
  '/// IAU2000B nutation-in-longitude terms (77 lunisolar).\n' +
  '/// Columns: [l, l\', F, D, Omega, dpsi_sin, dpsi_sin_T], the last two in\n' +
  '/// units of 0.1 microarcseconds. arg = sum(col[0..5] * delaunay), and\n' +
  '/// dpsi += (col[5] + col[6]*T) * sin(arg).\n' +
  `#[rustfmt::skip]\npub static NUT_COEFFS: &[[f64; 7]] = &[\n` +
  NUT.map((r) => '    [' + r.map(f).join(', ') + ']').join(',\n') +
  '\n];\n';
writeFileSync(join(root, 'rust/src/nutation_data.rs'), nutRs);

console.log(
  `EARTH_L: ${EARTH_L.reduce((n, s) => n + s.length, 0)} terms, ` +
  `EARTH_R: ${EARTH_R.reduce((n, s) => n + s.length, 0)} terms, ` +
  `NUT_COEFFS: ${NUT.length} rows`
);
