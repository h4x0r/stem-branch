#!/usr/bin/env node
/**
 * Generate the Rust↔npm parity golden corpus from the published npm engine, so
 * the Rust crate can assert it reproduces the TypeScript reference to f64 noise.
 *
 * Output: rust/tests/data/npm_golden.txt (line-oriented, dependency-free to parse
 * on the Rust side). Run: node scripts/gen-npm-golden.mjs
 */
import { writeFileSync, mkdirSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import {
  solarEclipticState,
  getMoonPosition,
  gregorianToLunisolar,
  solarTermForLongitude,
  deltaTForYear,
} from '../dist/index.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const lines = [
  '# Rust<->npm parity golden corpus — generated by scripts/gen-npm-golden.mjs.',
  '# solar <jde_tt> <true_deg> <app_deg> <radius_au>',
  '# moon <jde_tt> <lon_deg> <lat_deg> <dist_km>',
  '# lunisolar <gy> <gm> <gd> <ly> <lm> <ld> <leap0|1>',
  '# term <deg> <name>',
];

// Solar — jde grid across 1900–2100 at a non-round step (round and off-round instants).
for (let jde = 2415020.5; jde <= 2488070; jde += 211.317) {
  const s = solarEclipticState(jde);
  lines.push(`solar ${jde} ${s.trueLongitudeDegrees} ${s.apparentLongitudeDegrees} ${s.radiusAu}`);
}

// Moon — 1st of each month 1950–2050 at noon UT. jde_tt is reconstructed exactly
// as dateToJD_TT does, so the Rust side is fed the identical input getMoonPosition used.
for (let y = 1950; y <= 2050; y++) {
  for (let m = 0; m < 12; m++) {
    const d = new Date(Date.UTC(y, m, 1, 12, 0, 0));
    const jdUT = d.getTime() / 86400000 + 2440587.5;
    const decimalYear = d.getUTCFullYear() + (d.getUTCMonth() + 0.5) / 12;
    const jdeTT = jdUT + deltaTForYear(decimalYear) / 86400;
    const p = getMoonPosition(d);
    lines.push(`moon ${jdeTT} ${p.longitude} ${p.latitude} ${p.distance}`);
  }
}

// Lunisolar — quarterly (1st & 15th of Jan/Apr/Jul/Oct), 1950–2050. Parity is a
// fidelity check; the thorough calendar coverage lives in the npm cnlunar test
// (2424 dates) and the Rust published-anchor oracle, so a leaner grid suffices.
for (let y = 1950; y <= 2050; y++) {
  for (const m of [1, 4, 7, 10]) {
    for (const day of [1, 15]) {
      const r = gregorianToLunisolar(new Date(y, m - 1, day));
      lines.push(`lunisolar ${y} ${m} ${day} ${r.year} ${r.month} ${r.day} ${r.isLeapMonth ? 1 : 0}`);
    }
  }
}

// Solar terms — every 1°.
for (let deg = 0; deg < 360; deg += 1) {
  lines.push(`term ${deg} ${solarTermForLongitude(deg)}`);
}

const outDir = join(root, 'rust/tests/data');
mkdirSync(outDir, { recursive: true });
const out = join(outDir, 'npm_golden.txt');
writeFileSync(out, lines.join('\n') + '\n');
console.log(`wrote ${lines.length} lines to ${out}`);
